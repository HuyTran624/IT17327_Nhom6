    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        int category = cbbloai.getSelectedIndex();
        String value = txtvalue.getText();
        ArrayList<HoaDonHoanTraViewModel> list = repo.getlistdata();
        ArrayList<HoaDonHoanTraViewModel> final_list = new ArrayList<>();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        switch (category) {
            case 0 -> {
                for (HoaDonHoanTraViewModel hoaDonHoanTraViewModel : list) {
                    if (value.equalsIgnoreCase(hoaDonHoanTraViewModel.getId())) {
                        final_list.add(hoaDonHoanTraViewModel);
                    }
                }
            }
            case 1 -> {
                for (HoaDonHoanTraViewModel hoaDonHoanTraViewModel : list) {
                    if (value.equalsIgnoreCase(hoaDonHoanTraViewModel.getIdhd())) {
                        final_list.add(hoaDonHoanTraViewModel);
                    }
                }
            }
            case 2 -> {
                for (HoaDonHoanTraViewModel hoaDonHoanTraViewModel : list) {
                    try {
                        if (sdf.parse(value).equals(hoaDonHoanTraViewModel.getNgaytra()) == true) {
                            final_list.add(hoaDonHoanTraViewModel);
                        }
                    } catch (ParseException ex) {
                        ex.printStackTrace();
                        return;
                    }
                }
            }
            default -> {
            }
        }
        DefaultTableModel dtm = (DefaultTableModel) tblhoadon.getModel();
        dtm.setRowCount(0);
        for (HoaDonHoanTraViewModel h : final_list) {
            Object[] rowData = {
                h.getId(),
                h.getIdhd(),
                repo.getkh(h.getIdhd()).getTen(),
                convertdate(h.getNgaytra()),
                h.getDongia(),
                h.getSoluong(),
                h.getTinhtrang() == 0 ? "đã trả" : "chưa trả",
                h.getPhiphatsinh()
            };
            dtm.addRow(rowData);
        }
    }//GEN-LAST:event_btnsearchActionPerformed

